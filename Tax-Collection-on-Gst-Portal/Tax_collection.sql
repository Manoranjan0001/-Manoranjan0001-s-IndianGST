
	WITH CTE AS 
		(
			SELECT * FROM [dbo].[Tax-Collection-on-GST-Portal-2017-2018] 
	
			UNION 
			SELECT * FROM [dbo].[Tax-Collection-on-GST-Portal-2018-2019]

			UNION 
			SELECT * FROM [dbo].[Tax-Collection-on-GST-Portal-2019-2020]
				
			UNION
			SELECT * FROM [dbo].[Tax-Collection-on-GST-Portal-2020-2021]
			
			UNION 
			SELECT * FROM [dbo].[Tax-Collection-on-GST-Portal-2021-2022]
		

			UNION 
			SELECT * FROM [dbo].[Tax-Collection-on-GST-Portal-2022-2023]
		
		)

			SELECT  year AS [Financial Year],State ,round(sum(CGST+CGST2+CGST3+CGST4+CGST5+CGST6+CGST7+CGST8+CGST9+CGST10+CGST11+CGST12),2) as CGST,
						round(Sum(SGST+SGST2+SGST3+SGST4+SGST5+SGST6+SGST7+SGST8+SGST9+SGST10+SGST11+SGST12),2) AS SGST,
						round(sum(IGST+IGST2+IGST3+IGST4+IGST5+IGST6+IGST7+IGST8+IGST9+IGST10+IGST11+IGST12),2) AS IGST,
						round(sum(CESS+CESS2+CESS3+CESS4+CESS5+CESS6+CESS7+CESS8+CESS9+CESS10+CESS11+CESS12),2) AS CESS,

						round(SUM(CGST+CGST2+CGST3+CGST4+CGST5+CGST6+CGST7+CGST8+CGST9+CGST10+CGST11+CGST12+
						SGST+SGST2+SGST3+SGST4+SGST5+SGST6+SGST7+SGST8+SGST9+SGST10+SGST11+SGST12+
						IGST+IGST2+IGST3+IGST4+IGST5+IGST6+IGST7+IGST8+IGST9+IGST10+IGST11+IGST12+
						CESS+CESS2+CESS3+CESS4+CESS5+CESS6+CESS7+CESS8+CESS9+CESS10+CESS11+CESS12),2) as Total_Collection

			 FROM CTE
 
			GROUP BY  State, year

			order by Total_Collection  DESC





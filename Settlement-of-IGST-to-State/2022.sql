CREATE TABLE Settlement_of_IGST_to_State_2022_Collect
(YEAR VARCHAR(20),STATE VARCHAR(50),[1st Quater] MONEY  ,[2nd Quater] MONEY ,[3rd Quater] MONEY,[4th Quater] MONEY
)

INSERT INTO Settlement_of_IGST_to_State_2022_Collect


SELECT S23.YEAR,S23.State,
			ROUND(SUM(COALESCE(S12.Jan_22,0)),2)+ROUND(SUM(COALESCE(S12.Feb_22,0)),2)+ROUND(SUM(COALESCE(S12.Mar_22,0)),2) AS [1st Quater],
			ROUND(SUM(COALESCE(s23.APR_22,0)),2)+ROUND(SUM(COALESCE(s23.MAY_22,0)),2)+ROUND(SUM(COALESCE(s23.JUN_22,0)),2) AS [2nd Quater],
			ROUND(SUM(COALESCE(s23.JUL_22,0)),2)+ROUND(SUM(COALESCE(s23.AUG_22,0)),2)+ROUND(SUM(COALESCE(s23.SEP_22,0)),2) AS [3rd Quater],
			ROUND(SUM(COALESCE(s23.OCT_22,0)),2)+ROUND(SUM(COALESCE(s23.NOV_22,0)),2)+ROUND(SUM(COALESCE(s23.DEC_22,0)),2) AS [4th Quater]

FROM [dbo].[Settlement-of-IGST-to-State-2022-2023] as S23
LEFT JOIN [dbo].[Settlement-of-IGST-to-State-2021-2022] AS S12
ON S12.State = S23.State
GROUP BY S23.YEAR ,S23.State

